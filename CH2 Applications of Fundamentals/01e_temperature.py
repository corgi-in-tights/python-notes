# 2.01 Exercise: Temperature Converter

# Formulas:
# Celsius to Fahrenheit: F = (C × 9/5) + 32
# Fahrenheit to Celsius: C = (F - 32) × 5/9

temp = float(input("Enter celsius "))
converted = (temp * 9/5) + 32
print(f"{temp}°C = {converted}°F")

