# 1.01 Introduction to Data Types

# A value in programming can be of different types, known as data types
# Here are some of the most common data types in Python:

# Integer
age = 25
steps = -2

# String
name = "Alice, Smith"
last_name = 'Johnson'

# Float
height = 0.575
width = 10.0

# Boolean
is_student = True 
is_employed = False 

# Notice how we assign each value to a variable using the '=' operator
# Variables are like labels that help us refer to these values later in the code

is_student # Anytime we use this 'variable' now, it will refer to 'True'
age # Anytime we use this 'variable' now, it will refer to '25'

